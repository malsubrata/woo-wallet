#!/usr/bin/env bash
#
# Provision the TeraWallet integration test environment.
#
# Creates an isolated test database and generates wp-tests-config.php. The
# WordPress *core* and the WP test framework are reused (core from the
# existing install, framework from vendor/wp-phpunit), so this script only
# has to set up the database and config.
#
# No `mysql` CLI client is required — the test database is created through
# WP-CLI, which talks to MySQL via PHP's mysqli driver.
#
# Usage: bash bin/install-wp-tests.sh
#
set -euo pipefail

PLUGIN_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )/.." && pwd )"
WP_CORE_DIR="$( cd "${PLUGIN_DIR}/../../.." && pwd )"
TEST_DB_NAME="${WP_WALLET_TEST_DB:-wp_terawallet_tests}"

WP=( wp --path="${WP_CORE_DIR}" --allow-root )

if ! command -v wp >/dev/null 2>&1; then
	echo "WP-CLI ('wp') is required but was not found on PATH." >&2
	exit 1
fi

if [ ! -f "${WP_CORE_DIR}/wp-load.php" ]; then
	echo "WordPress core not found at ${WP_CORE_DIR}." >&2
	exit 1
fi

if [ ! -d "${PLUGIN_DIR}/vendor/wp-phpunit/wp-phpunit" ]; then
	echo "vendor/wp-phpunit not found. Run 'composer install' first." >&2
	exit 1
fi

# Reuse the live site's database credentials; only the database name differs.
DB_USER="$( "${WP[@]}" config get DB_USER )"
DB_PASSWORD="$( "${WP[@]}" config get DB_PASSWORD )"
DB_HOST="$( "${WP[@]}" config get DB_HOST )"

# Create the isolated test database (safe to re-run).
"${WP[@]}" db query "CREATE DATABASE IF NOT EXISTS \`${TEST_DB_NAME}\`"
echo "Test database '${TEST_DB_NAME}' is ready."

# Generate wp-tests-config.php. Note: a single quote in DB_PASSWORD would
# break the generated PHP — fine for typical local dev credentials.
cat > "${PLUGIN_DIR}/wp-tests-config.php" <<PHP
<?php
/**
 * Generated by bin/install-wp-tests.sh — do not edit, do not commit.
 */
define( 'ABSPATH', '${WP_CORE_DIR}/' );
define( 'WP_DEFAULT_THEME', 'default' );

define( 'DB_NAME', '${TEST_DB_NAME}' );
define( 'DB_USER', '${DB_USER}' );
define( 'DB_PASSWORD', '${DB_PASSWORD}' );
define( 'DB_HOST', '${DB_HOST}' );
define( 'DB_CHARSET', 'utf8' );
define( 'DB_COLLATE', '' );

\$table_prefix = 'wptests_';

define( 'WP_TESTS_DOMAIN', 'example.org' );
define( 'WP_TESTS_EMAIL', 'admin@example.org' );
define( 'WP_TESTS_TITLE', 'TeraWallet Test Suite' );
define( 'WP_PHP_BINARY', 'php' );
PHP

echo "wp-tests-config.php written to ${PLUGIN_DIR}."
echo "Run 'composer test' to execute the suite."
